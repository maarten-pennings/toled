void toled_mono5(char ch, int * width, int * height, const uint8_t ** bmp ) {
  if( ch<' ' || ch> '~' ) {
    *width = 0;
    *height= 0;
    *bmp   = 0;
  } else {
    int ix = ch - ' ';
    *width = toled_mono5_dsc[ix][0];
    *height= toled_mono5_dsc[ix][1];
    *bmp   = &toled_mono5_bmp[ toled_mono5_dsc[ix][2] ];
  }
}
